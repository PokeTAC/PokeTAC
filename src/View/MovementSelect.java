    private void displayCurrentPokemon(){
        PokeInfo p = pokeinfos.get(index);
        try {
            PokeTAC.loadDataPokeMovements(p);
        }
        catch(IOException e){
            System.out.println("ERROR: PokeMovements loader");
            e.printStackTrace();
        }
        List<Movement> movs = p.getMoves();
        DefaultListModel<Movement> model = new DefaultListModel<>();
        movs.stream().forEach((m) -> {model.addElement(m);});
        jlstMovements.setModel(model);
        lblName.setText(p.getNombre());
        if(index == 3) btnNext.setText("Finalizar");
        else btnNext.setText("Siguiente");
    }
    
    public List<Pokemon> displayMovementSelect(){
        this.setVisible(true);
        if(pokemons.size() == PokeTAC.MAX_POKEMON){
            return pokemons;
        }else{
            return null;
        }
    }

