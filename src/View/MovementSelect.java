    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        PokeInfo p = pokeinfos.get(index);
        List<Movement> movs = jlstMovements.getSelectedValuesList();
        if(movs.size()==PokeTAC.MAX_MOVES){
            Pokemon pokemon = new Pokemon(p, movs);
            pokemons.add(pokemon);
            index++;
            if(index<PokeTAC.MAX_POKEMON){
                displayCurrentPokemon();
            }else{
                setVisible(false);
                dispose();
            }           
        }
    }//GEN-LAST:event_btnNextActionPerformed

