    public void startBattle(List<Pokemon> userPokemons){
        logicMan.initBattle(userPokemons);
        pnlBackground.remove(prevPanel);
        BattleScreen bscreen = new BattleScreen(this, logicMan);
        prevPanel = bscreen;
        pnlBackground.add(bscreen, BorderLayout.CENTER);
        this.revalidate();
    }
    
    public void restart(){
        pnlBackground.remove(prevPanel);
        pnlBackground.add(pnlWelcome,BorderLayout.CENTER);
        this.revalidate();
        this.repaint();
    }
    
    public AIType getAIType(){
        return aiType;
    }
    

